package com.taole.framework.exception;

import org.apache.commons.lang.exception.NestableRuntimeException;

/**
 * 有重复数据时抛出。
 * 
 * @author Rory
 * @date Nov 16, 2011
 * @version $Id: NotUniqueException.java 5 2014-01-15 13:55:28Z yedf $
 */
public class NotUniqueException extends NestableRuntimeException {

	private static final long serialVersionUID = 7817596825753332685L;

	public NotUniqueException() {
		super();
	}

	public NotUniqueException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public NotUniqueException(String msg) {
		super(msg);
	}

	public NotUniqueException(Throwable cause) {
		super(cause);
	}

	
}
