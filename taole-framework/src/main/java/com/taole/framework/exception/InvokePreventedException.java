package com.taole.framework.exception;

public class InvokePreventedException extends Exception {

	private static final long serialVersionUID = 1736053732677707135L;

	public InvokePreventedException() {}

	public InvokePreventedException(String message) {
		super(message);
	}

	public InvokePreventedException(Throwable cause) {
		super(cause);
	}

	public InvokePreventedException(String message, Throwable cause) {
		super(message, cause);
	}

}
